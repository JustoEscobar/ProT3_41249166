<div id="elaboracion" class="container mt-5 justify-text">
    <h2>Proceso de Elaboración de Nuestros Productos</h2>
    <p>
        En Coffee House, cada paso en la elaboración de nuestros productos es crucial para garantizar la más alta calidad y sabor. Aquí te mostramos cómo llevamos a cabo este meticuloso proceso:
    </p>
    <h3>1. Selección de Granos</h3>
    <p>
        Todo comienza con la selección de los granos de café. Trabajamos directamente con agricultores de las mejores regiones cafeteras del mundo. Seleccionamos cuidadosamente los granos que cumplen con nuestros estrictos estándares de calidad y sostenibilidad.
    </p>
    <h3>2. Tostado Perfecto</h3>
    <p>
        Nuestros expertos tostadores utilizan métodos precisos para resaltar las características únicas de cada variedad de grano. El tostado se realiza en pequeños lotes para garantizar un control óptimo de la calidad y el sabor.
    </p>
    <h3>3. Molienda Fresca</h3>
    <p>
        La molienda es un paso esencial para obtener el mejor sabor en cada taza. Mantenemos la frescura del café moliéndolo justo antes de su preparación, adaptando el grosor de la molienda según el método de elaboración que se utilizará.
    </p>
    <h3>4. Métodos de Preparación</h3>
    <p>
        Ofrecemos una variedad de métodos de preparación, desde el clásico espresso hasta métodos más artesanales como el pour-over y la prensa francesa. Cada método es ejecutado con precisión para destacar los sabores únicos de nuestros granos.
    </p>
    <h3>5. Innovación y Creatividad</h3>
    <p>
        Constantemente innovamos y experimentamos con nuevas recetas y técnicas. Desde lattes y cappuccinos hasta nuestras bebidas de temporada, cada creación es el resultado de nuestra pasión por el café y la búsqueda de nuevas experiencias para nuestros clientes.
    </p>
    <h3>6. Servicio con Amor</h3>
    <p>
        Finalmente, cada taza de café es servida con dedicación y atención al detalle. Nuestro equipo está comprometido en ofrecer un servicio excepcional y en hacer de cada visita a Coffee House una experiencia memorable.
    </p>
    <p>
        En Coffee House, no solo hacemos café; creamos momentos especiales. Gracias por acompañarnos en este viaje de sabor y calidad.
    </p>
</div>

